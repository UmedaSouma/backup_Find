#include "speedmeter.h"
#include "manager.h"
#include "donut.h"

//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CSpeedMeter::CSpeedMeter()
{
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CSpeedMeter::~CSpeedMeter()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CSpeedMeter::Init()
{
	int Idx = 0;
	Idx = CManager::GetTexture()->Regist("data\\TEXTURE\\Speedmeter_000.png");
	BindTexture(CManager::GetTexture()->GetAddress(Idx));

	SetPos({ SCREEN_WIDTH * 0.15f,SCREEN_HEIGHT * 0.77f,0.0f });
	SetSize({ 140.0f,90.0f,0.0f });

	CDonut::Create(GetPos());

	CObject2D::Init();
	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CSpeedMeter::Uninit()
{
	CObject2D::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CSpeedMeter::Update()
{
	CObject2D::Update();
}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CSpeedMeter::Draw()
{
	CObject2D::Draw();
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CSpeedMeter* CSpeedMeter::Create()
{
	CSpeedMeter* pSpeedMeter = new CSpeedMeter;
	pSpeedMeter->Init();

	return pSpeedMeter;
}