#include "tree.h"

//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CTree::CTree()
{
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CTree::~CTree()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CTree::Init()
{
	CModel::Init();
	SetModel("data\\model\\tree_001.x");

	SetPos({ 4000.0f,-2000.0f,1500.0f });
	SetScale(GetScale() * 0.5f);

	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CTree::Uninit()
{
	CModel::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CTree::Update()
{
	CModel::Update();
}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CTree::Draw()
{
	CModel::Draw();
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CTree* CTree::Create()
{
	CTree* pTree = new CTree;
	pTree->Init();

	return pTree;
}