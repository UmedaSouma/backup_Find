#ifndef _STONE_H_
#define _STONE_H_
#include "main.h"
#include "model.h"

class CStone :public CModel
{
public:
	CStone();
	~CStone()override;
	HRESULT Init()override;	// 初期設定
	void Uninit()override;	// 終了
	void Update()override;	// 更新
	void Draw()override;	// 描画

	static CStone* Create();   // 生成処理
private:

};

//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CStone::CStone()
{
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CStone::~CStone()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CStone::Init()
{
	SetModel("data\\MODEL\\stone_000.x");

	CModel::Init();
	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CStone::Uninit()
{
	CModel::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CStone::Update()
{
	CModel::Update();
}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CStone::Draw()
{
	CModel::Draw();
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CStone* CStone::Create()
{
	CStone* pStone = new CStone;
	pStone->Init();

	return pStone;
}

#endif // !STONE_H_