#include "actor.h"

//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CActor::CActor()
{
	m_bGravity = false;
	m_move = { 0.0f,0.0f,0.0f };
	m_oldpos = { 0.0f,0.0f,0.0f };
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CActor::~CActor()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CActor::Init()
{
	CModel::Init();

	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CActor::Uninit()
{
	CModel::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CActor::Update()
{
	m_oldpos = GetPos();

	if (m_bGravity)
	{
		Gravity();
	}

	CModel::Update();
}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CActor::Draw()
{
	CModel::Draw();
}

//===========================================================================================================
// 重力処理
//===========================================================================================================
void CActor::Gravity()
{
	m_move.y -= GRAVITY;	// 重力加算
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CActor* CActor::Create()
{
	CActor* pActor = new CActor;

	pActor->Init();

	return pActor;
}
