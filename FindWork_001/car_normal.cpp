//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CCarNormal::CCarNormal()
{
	SetWeight(MAX_WEIGHT);
	SetGearNum(MAX_GEAR);
	SetBending(MAX_BENDING);
	SetMaxLife(MAX_LIFE);

	SetTypeCar(CParamStorage::CAR_NORMAL);
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CCarNormal::~CCarNormal()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CCarNormal::Init()
{
	CCar::Init();

	//SetModel("data\\model\\car_sample.x");

	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CCarNormal::Uninit()
{
	CCar::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CCarNormal::Update()
{
	CCar::Update();
}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CCarNormal::Draw()
{
	CCar::Draw();
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CCarNormal* CCarNormal::Create(D3DXVECTOR3 pos)
{
	CCarNormal* pCarNormal = new CCarNormal;

	pCarNormal->Init();

	return pCarNormal;
}