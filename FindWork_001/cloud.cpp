#include "cloud.h"

//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CCloud::CCloud()
{
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CCloud::~CCloud()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CCloud::Init()
{
	CModel::Init();
	SetModel("data\\model\\cloud_001.x");

	SetPos({ 4000.0f,-1000.0f,1500.0f });
	SetScale(GetScale() * 0.5f);

	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CCloud::Uninit()
{
	CModel::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CCloud::Update()
{
	CModel::Update();

	D3DXVECTOR3 rot = GetRot();
	rot.y += 0.001f;

	SetRot(rot);
}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CCloud::Draw()
{
	CModel::Draw();
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CCloud* CCloud::Create()
{
	CCloud* pCloud = new CCloud;
	pCloud->Init();

	return pCloud;
}