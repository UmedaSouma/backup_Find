#include "title_poly.h"
#include "manager.h"

//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CTitlePoly::CTitlePoly(int Prio):CObject2D(Prio)
{
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CTitlePoly::~CTitlePoly()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CTitlePoly::Init()
{
	SetSize({ SCREEN_WIDTH * 0.5f,SCREEN_HEIGHT * 0.5f,0.0f });

	int Idx = 0;
	Idx = CManager::GetTexture()->Regist("data\\TEXTURE\\title.png");
	BindTexture(CManager::GetTexture()->GetAddress(Idx));

	SetPos({ SCREEN_WIDTH * 0.5f,SCREEN_HEIGHT * 0.5f,0.0f });

	CObject2D::Init();
	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CTitlePoly::Uninit()
{
	CObject2D::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CTitlePoly::Update()
{
	CObject2D::Update();
}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CTitlePoly::Draw()
{
	CObject2D::Draw();
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CTitlePoly* CTitlePoly::Create()
{
	CTitlePoly* pTitlePoly = new CTitlePoly;
	pTitlePoly->Init();

	return pTitlePoly;
}