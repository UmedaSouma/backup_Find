#include "course_point.h"

//========================================================================================================================
// コンストラクタ
//========================================================================================================================
CCoursePoint::CCoursePoint() :
	m_nIdxPoint(-1)
{
}

//========================================================================================================================
// デストラクタ
//========================================================================================================================
CCoursePoint::~CCoursePoint()
{
}

//========================================================================================================================
// 初期設定
//========================================================================================================================
HRESULT CCoursePoint::Init()
{
#ifdef _DEBUG
	SetModel("data\\MODEL\\course_point_000.x");
#endif // !_DEBUG

	SetType(CObject::POINT);
	CModel::Init();

	return S_OK;
}

//========================================================================================================================
// 終了処理
//========================================================================================================================
void CCoursePoint::Uninit()
{
	CModel::Uninit();
}

//========================================================================================================================
// 更新処理
//========================================================================================================================
void CCoursePoint::Update()
{
	CModel::Update();
}

//========================================================================================================================
// 描画処理
//========================================================================================================================
void CCoursePoint::Draw()
{
	CModel::Draw();
}

//========================================================================================================================
// 生成処理
//========================================================================================================================
CCoursePoint* CCoursePoint::Create(int Idx,D3DXVECTOR3 pos, D3DXVECTOR3 rot)
{
	CCoursePoint* pCoursePoint = new CCoursePoint;

	// インデックスで順番を設定
	pCoursePoint->m_nIdxPoint = Idx;
	pCoursePoint->SetPos(pos);
	pCoursePoint->SetRot(rot);

	pCoursePoint->Init();

	return pCoursePoint;
}